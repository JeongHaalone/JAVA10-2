package ch02;

public class GarbageExam {

	public static void main(String[] args) {
		// 연습문제의 오류를 보완한 코딩
		
		int a = 4;
		float b = 3F;
		int c = (int) (++a + -b); //++(증가연산자) 와 -를 합한 값 -> 4-3
	
		System.out.println(c);

		
		
		
		
		int apple =3;
		int bannana =5;
		double carrot= apple/bannana;
		System.out.println("------------------------");
		System.out.println("출력 결과 : " +carrot);
		
		
		
	
		
		
		
	}

}
